clear

b = 10;
c1 = 1;
c2 = 1;

# y' = x
# x' = 16.81*y
# (y, x)

m = @(x, y)([y(2); 16.81*y(1)]);  #свел уравнение к системе

[t, y] = ode45(m, [0, b], [1, -4.1]);

# точное решение
x = 0:0.01:b;
y2 = exp(x.*4.1).*c1 + exp(x.*4.1).^(-1).*c2;

hold on
plot(t, y(:, 1));
plot(x, y2);
